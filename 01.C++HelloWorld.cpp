#include <iostream>

using namespace std;

int main()
{
    cout << "Hello World" << endl;

    // without using namespace std;
    std::cout << "Hello C++" << std::endl;

    // letter
    std::cout << "Dear Self,            \n";
    std::cout << "                      \n";
    std::cout << "I will build a VR app.\n";
    std::cout << "                      \n";
    std::cout << "                -Sonny\n";

    // pattern
    std::cout << "       1\n";
    std::cout << "     2 3\n";
    std::cout << "   4 5 6\n";
    std::cout << "7 8 9 10\n";

    // spell
    std::cout << "Expecto Patronum\n";

    return 0;
}
